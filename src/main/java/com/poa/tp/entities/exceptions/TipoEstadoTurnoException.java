package com.poa.tp.entities.exceptions;

public class TipoEstadoTurnoException extends EntidadesException {

	private static final long serialVersionUID = 1L;

	public TipoEstadoTurnoException() {
		super();
	}

	public TipoEstadoTurnoException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TipoEstadoTurnoException(String message, Throwable cause) {
		super(message, cause);
	}

	public TipoEstadoTurnoException(String message) {
		super(message);
	}

	public TipoEstadoTurnoException(Throwable cause) {
		super(cause);
	}	
}
